# Tipos de datos básicos

# Number (números)
age = 24 # integer
height = 1.80 # float
complex = 7 + 5j # complex

# String (cadena de texto)
name = "Arsène"
last_name = 'Lupin'

# Boolean (booleanos)
studing = True
work_in_silicon_valley = False 

print(type(age))
print(type(height))
print(type(complex))

print(type(name))
print(type(last_name))

print(type(studing))
print(type(work_in_silicon_valley))